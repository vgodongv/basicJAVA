package e_oop;

public class Calculator {
	 // 파라미터(계산할때 필요한 값 2개)와 리턴타입(두개의 파라미터를 받아 연산한 결과-알수없으므로 double타입이 적합.) 메서드 5개
	   
	   long add(long x,long y){
	      return x+y;
	   }
	   long sub(long x,long y){
	      return x-y;
	   }
	   long mul(long x,long y){
	      return x*y;
	   }
	   long div(long x,long y){
	      return x/y;
	   }
	    long remain(long x,long y){
	      return x%y;
	   }
	   
	}